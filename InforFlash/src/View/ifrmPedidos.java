    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (txtCli.getText().trim().isEmpty() || cbStatus.getSelectedIndex() == -1 || Integer.parseInt(String.valueOf(spiQtd.getValue())) == 0) {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios! ", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {

            if (!txtCod.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Esse pedido já existe. \nClique no botão de limpar para cadastrar um novo pedido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            } else {
                int op = JOptionPane.showConfirmDialog(null, String.format("Deseja mesmo cadastrar o pedido?", txtCli.getText().trim()), "Confirme", JOptionPane.YES_NO_OPTION);
                if (op == JOptionPane.YES_OPTION) {

                    Pedidos pedido = new Pedidos();
                    ConPedidos conPedido = new ConPedidos();

                    Atendimentos atendimento = new Atendimentos();
                    ConAtendimentos conAtend = new ConAtendimentos();

                    ConServicos conServ = new ConServicos();
                    Servicos servico = new Servicos();

                    ConFuncionarios conFunc = new ConFuncionarios();
                    Funcionarios func = new Funcionarios();

                    Login login = new Login();

                    conFunc.pegarFuncionario(login.getUsuario());

                    pedido.setCliente(Integer.parseInt(txtCli.getText().trim()));
                    pedido.setFuncionario(func.getCodigo());
                    pedido.setHora(txtHora.getText().trim());
                    pedido.setData(txtData.getText().trim());
                    pedido.setStatus(cbStatus.getSelectedItem().toString());

                    if (txtData.getText().trim().isEmpty()) {
                        pedido.setData(" ");
                    } else {
                        pedido.setData(txtData.getText().trim());
                    }

                    if (txtHora.getText().trim().isEmpty()) {
                        pedido.setHora(" ");
                    } else {
                        pedido.setHora(txtHora.getText().trim());
                    }

                    conPedido.cadastrar(pedido);

                    atendimento.setPedido(conAtend.pegarUltimoPedido());
                    atendimento.setServico(cbTipos.getSelectedIndex() + 1);

                    atendimento.setObs(txtObs.getText());
                    atendimento.setQtd(Integer.parseInt(String.valueOf(spiQtd.getValue())));

                    servico = conServ.pesquisarTipoServ(cbTipos.getSelectedIndex() + 1);

                    float preco = servico.getValor() * Integer.parseInt(String.valueOf(spiQtd.getValue()));

                    atendimento.setPreco(preco);

                    conAtend.cadastrar(atendimento);

                    JOptionPane.showMessageDialog(null, "Pedido cadastrado com sucesso!");

                    limpar();
                    listar();

                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

