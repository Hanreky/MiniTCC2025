    public void pegarPedido(int codigo) {
        try {

            Pedidos pedido = new Pedidos();
            ConPedidos conpedido = new ConPedidos();

            Atendimentos atendimento = new Atendimentos();
            ConAtendimentos conAtend = new ConAtendimentos();

            ConFuncionarios conFunc = new ConFuncionarios();
            Funcionarios func = new Funcionarios();

            pedido = conpedido.pegarPedido(codigo);
            atendimento = conAtend.pegarAtendimento(codigo);

            txtCod.setText(String.valueOf(pedido.getCodigo()));
            txtCli.setText(String.valueOf(pedido.getCliente()));
            txtHora.setText(pedido.getHora());
            txtData.setText(pedido.getData());
            txtNomeCli.setText(pedido.getNomeCiente());

            String status = pedido.getStatus();

            switch (status) {
                case "Em preparação":
                    cbStatus.setSelectedIndex(0);
                    break;
                case "Aguardando Coleta":
                    cbStatus.setSelectedIndex(1);
                    break;
                case "Finalizado":
                    cbStatus.setSelectedIndex(2);
                    break;
            }

            cbTipos.setSelectedIndex(atendimento.getServico() - 1);
            spiQtd.setValue(atendimento.getQtd());
            txtPreco.setText(Formatacoes.formatarMoeda(atendimento.getPreco()));
            txtObs.setText(atendimento.getObs());

            func = conFunc.pegarFuncionario(func);

            txtFunc.setText(func.getNome());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

