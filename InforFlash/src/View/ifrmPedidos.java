    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        try {
            if (txtCod.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Escolha um pedido!", "Aviso", JOptionPane.WARNING_MESSAGE);
            } else {
                int op = JOptionPane.showConfirmDialog(null, String.format("Deseja mesmo cancelar o pedido?", txtCod.getText().trim()), "Confirme", JOptionPane.YES_NO_OPTION);
                if (op == JOptionPane.YES_OPTION) {
                    ConPedidos conPedido = new ConPedidos();
                    Pedidos pedido = new Pedidos();

                    int codigo = Integer.parseInt(txtCod.getText());

                    pedido = conPedido.pegarPedido(codigo);

                    conPedido.cancelar(codigo);

                    JOptionPane.showMessageDialog(null, "Pedido de ID " + pedido.getCodigo() + " Cancelado.", "Aviso", JOptionPane.WARNING_MESSAGE);

                    limpar();
                    listar();

                    //validar();
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            Pedidos pedido = new Pedidos();
            ConPedidos conPedido = new ConPedidos();

            if (txtCod.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Selecione um pedido!", "Aviso", JOptionPane.WARNING_MESSAGE);
            } else if (cbStatus.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Status não selecionado!", "Aviso", JOptionPane.WARNING_MESSAGE);
            } else {
                int op = JOptionPane.showConfirmDialog(null, "Deseja mesmo editar o status do pedido?", "Confirme", JOptionPane.YES_NO_OPTION);
                if (op == JOptionPane.YES_OPTION) {
                    pedido.setCodigo(Integer.parseInt(txtCod.getText()));

                    pedido.setStatus(cbStatus.getSelectedItem().toString());
                    conPedido.editar(pedido);

                    JOptionPane.showMessageDialog(null, "Status atualizado com sucesso!");

                    listar();
                    limpar();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

