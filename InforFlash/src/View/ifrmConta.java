    private void tbtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbtnEditarActionPerformed
        if (tbtnEditar.isSelected()) {
            txtNome.setEnabled(true);
            txtTelefone.setEnabled(true);
            txtEmail.setEnabled(true);
            
            tgBtnSenha.setEnabled(true);
            tbtnNovaSenha.setEnabled(true);
        }
        if (!tbtnEditar.isSelected()) {
            try {
                Login login = new Login();
                ConLogin conLogin = new ConLogin();

                Funcionarios funcionario = new Funcionarios();
                ConFuncionarios conFunc = new ConFuncionarios();

                if (txtTelefone.getText().trim().isEmpty() || txtNome.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios!", "Aviso", JOptionPane.WARNING_MESSAGE);
                } else {
                    int op = JOptionPane.showConfirmDialog(null, String.format("Deseja mesmo editar %s?", txtNome.getText().trim()), "Confirme", JOptionPane.YES_NO_OPTION);
                    if (op == JOptionPane.YES_OPTION) {
                        funcionario.setCodigo(Integer.parseInt(txtCod.getText()));
                        funcionario.setNome(txtNome.getText());
                        funcionario.setTelefone(txtTelefone.getText());
                        funcionario.setCpf(txtCpf.getText());
                        funcionario.setEmail(txtEmail.getText());
                        conFunc.editar(funcionario);
                        
                        if(tbtnNovaSenha.isSelected() && !String.valueOf(pssConfirmarSenha.getPassword()).trim().isEmpty()){
                            login.setUsuario(txtUsuario.getText());
                            login.setSenha(String.valueOf(pssConfirmarSenha.getPassword()).trim());
                            
                            conLogin.editar(login);
                        }

                        JOptionPane.showMessageDialog(null, "Perfil atualizado com sucesso!");

                        txtNome.setEnabled(false);
                        txtTelefone.setEnabled(false);
                        txtCpf.setEnabled(false);
                        txtEmail.setEnabled(false);
                        txtUsuario.setEnabled(false);
                        pssSenha.setEnabled(false);
                        tgBtnSenha.setEnabled(false);
                        tbtnNovaSenha.setEnabled(false);

                        pssConfirmarSenha.setVisible(false);
                        tgBtnConfirmarSenha.setVisible(false);
                        jLabel7.setVisible(false);

                        listar();
                        // limpar();
                    } else {
                        txtNome.setEnabled(false);
                        txtTelefone.setEnabled(false);
                        txtCpf.setEnabled(false);
                        txtEmail.setEnabled(false);
                        txtUsuario.setEnabled(false);
                        pssSenha.setEnabled(false);
                        tgBtnSenha.setEnabled(false);
                        tbtnNovaSenha.setEnabled(false);

                        pssConfirmarSenha.setVisible(false);
                        tgBtnConfirmarSenha.setVisible(false);
                        jLabel7.setVisible(false);
                        listar();
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
    }//GEN-LAST:event_tbtnEditarActionPerformed

