    public void listar() {
        Login login = new Login();
        ConLogin conLogin = new ConLogin();

        txtUsuario.setText(login.getUsuario());
        login = conLogin.listar();
        pssSenha.setText(login.getSenha());

        Funcionarios funcionario = new Funcionarios();
        ConFuncionarios conFunc = new ConFuncionarios();

        funcionario = conFunc.listar(funcionario);
        txtCod.setText(String.valueOf(funcionario.getCodigo()));
        txtNome.setText(funcionario.getNome());
        txtTelefone.setText(funcionario.getTelefone());
        txtCpf.setText(funcionario.getCpf());
        txtEmail.setText(funcionario.getEmail());
    }

