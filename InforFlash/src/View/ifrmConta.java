    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {

            int op = JOptionPane.showConfirmDialog(null, String.format("Deseja mesmo excluir excluir sua conta, %s?", txtNome.getText().trim()), "Confirme", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                ConFuncionarios conFunc = new ConFuncionarios();

                int codigo = Integer.parseInt(txtCod.getText());

                conFunc.excluir(codigo);

                JOptionPane.showMessageDialog(null, "Conta excluida.", "Aviso", JOptionPane.WARNING_MESSAGE);
                
                Login login = new Login();
                login.setLoginValidado(false);

                this.setVisible(false);
                this.dispose();

                
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


